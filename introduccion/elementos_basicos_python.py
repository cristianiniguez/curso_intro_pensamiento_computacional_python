# Obteniendo el tipo de objeto

my_int = 1
my_float = 1.0
my_bool = False
my_none = None

type(my_int)  # <class 'int'>
type(my_float)  # <class 'float'>
type(my_bool)  # <class 'bool'>
type(my_none)  # <class 'NoneType'>

